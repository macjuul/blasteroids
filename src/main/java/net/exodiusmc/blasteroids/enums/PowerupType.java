package net.exodiusmc.blasteroids.enums;

public enum PowerupType {
	HEALTH_PACK,		// Increases your HP
	SPEED_BOOST,		// Increases your movement speed
	SUPER_BLASTER,		// Fires a 3 bullet burst when shot
	WIDE_SPREAD,		// Shoots 2 extra bullets on each side, going into a slightly altered direction
	PIERCING_BULLETS,	// Bullets will travel through asteroids
	SHIELD,				// Reduces incoming damage with 40%
	ENERGY_BURST;		// Breaks all asteroids currently within sight
}
